#include <stdio.h>

int main()
{
	char a = 3;
	//00000000000000000000000000000011 - 
	//截断
	//00000011 - 3 <- 实际存储
	char b = 127;
	//00000000000000000000000001111111
	//截断
	//01111111  -127<-实际存储

	char c = a + b;
    //00000011
	//01111111
	//--------
	//    |
	//整型提升：是按照变量的数据类型的符号位来提升的
	//00000000000000000000000000000011
	//00000000000000000000000001111111
	//00000000000000000000000010000010
	//--------
	//    |
	//10000010 -- c
	//11111111111111111111111110000010 - 补码
	//11111111111111111111111110000001 - 反码
	//10000000000000000000000001111110 - 原码（符号位不变，其余取反）
	//1111110 -- //-126
	printf("%d\n", c);
	return 0;
}